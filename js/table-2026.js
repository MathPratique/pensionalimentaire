/**
 * Calcule la contribution alimentaire parentale de base.
 * @param {number} revenuDispoTotal - Revenu disponible combiné des deux parents
 * @param {number} nombreEnfants - Nombre d'enfants (1 à 6+)
 * @returns {number} Contribution annuelle de base en CAD
 */
function getContributionBase(revenuDispoTotal, nombreEnfants) {
  if (revenuDispoTotal <= 0 || nombreEnfants <= 0) return 0;

  // Cas > 6 enfants: extrapolation selon différence colonne 5→6
  if (nombreEnfants > 6) {
    const contrib5 = getContributionBase(revenuDispoTotal, 5);
    const contrib6 = getContributionBase(revenuDispoTotal, 6);
    const enfantsAdd = nombreEnfants - 6;
    return contrib6 + ((contrib6 - contrib5) * enfantsAdd);
  }

  const col = nombreEnfants - 1;

  // Cas > 200 000 $
  if (revenuDispoTotal > TABLE_FIXATION_2026.formuleSuperieure.seuil) {
    const excedent = revenuDispoTotal - TABLE_FIXATION_2026.formuleSuperieure.seuil;
    const base = TABLE_FIXATION_2026.formuleSuperieure.montantsBase[col];
    const pct = TABLE_FIXATION_2026.formuleSuperieure.pourcentages[col];
    return Math.round(base + (excedent * pct));
  }

  // Recherche dans les paliers
  for (const [seuil, montants] of TABLE_FIXATION_2026.paliers) {
    if (revenuDispoTotal <= seuil) {
      return montants[col];
    }
  }
  return 0;
}

